#include <stdio.h>
int main ( )
{
  int a=66,b=79,c=89;                    // 定义3个字符变量，并初始化   
  putchar(a);                            // 向显示器输出字符B  
  putchar(b);                            // 向显示器输出字符O  
  putchar(c);                            // 向显示器输出字符Y  
  putchar ('\n');                        // 向显示器输出一个换行符  
  return 0;
 }




